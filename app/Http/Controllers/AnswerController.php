    {
        $validateData = $request->validate([
            'body' => 'required',
        ]);


        $answer = new Answer;
        $answer->user_id = Auth::id();
        $answer->question_id = $request->question_id;
        $answer->votes_count = $request->votes_count;
        $answer->body = $request->body;
        $answer->save();
        return redirect()->route('questions.index');
    }

